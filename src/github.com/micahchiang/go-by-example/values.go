package main

import "fmt"

func main() {
	// string concatenation
	fmt.Println("go" + "lang")

	// integer and float math
	fmt.Println("1+1 =", 1+1)
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	// booleans and boolean operators
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	fmt.Println(!false)
}
